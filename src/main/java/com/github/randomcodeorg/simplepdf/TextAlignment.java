package com.github.randomcodeorg.simplepdf;

/**
 * An enumeration to specify the text alignment.
 * @author Marcel Singer
 *
 */
public enum TextAlignment {

	/**
	 * The text will be aligned on the left hand side.
	 */
	LEFT,
	/**
	 * The text will be justified.
	 */
	JUSTIFIED,
	/**
	 * The text will be aligned on the right hand side.
	 */
	RIGHT,
	/**
	 * The text will be centered.
	 */
	CENTER
	
	
}
